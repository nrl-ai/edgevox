class TestMemoryTools:
    def test_remember_persists_to_store(self, tmp_path: Path):
        store = JSONMemoryStore(tmp_path / "m.json")
        remember, _forget, _recall = memory_tools(store)
        result = remember.call({"key": "user.name", "value": "Ada"})
        assert "remembered" in result
        assert store.get_fact("user.name") == "Ada"

    def test_forget_revokes_fact(self, tmp_path: Path):
        store = JSONMemoryStore(tmp_path / "m.json")
        store.add_fact("user.name", "Ada")
        _, forget, _ = memory_tools(store)
        result = forget.call({"key": "user.name"})
        assert "forgot" in result
        assert store.get_fact("user.name") is None

    def test_forget_missing_key_reports(self, tmp_path: Path):
        store = JSONMemoryStore(tmp_path / "m.json")
        _, forget, _ = memory_tools(store)
        result = forget.call({"key": "never.set"})
        assert "nothing to forget" in result

    def test_recall_returns_value(self, tmp_path: Path):
        store = JSONMemoryStore(tmp_path / "m.json")
        store.add_fact("x", "42")
        _, _, recall = memory_tools(store)
        assert recall.call({"key": "x"}) == "42"

    def test_recall_missing_returns_explanatory_string(self, tmp_path: Path):
        store = JSONMemoryStore(tmp_path / "m.json")
        _, _, recall = memory_tools(store)
        result = recall.call({"key": "missing"})
        assert "no fact" in result

    def test_schemas_are_wellformed_openai(self, tmp_path: Path):
        store = JSONMemoryStore(tmp_path / "m.json")
        for t in memory_tools(store):
            schema = t.openai_schema()
            assert schema["type"] == "function"
            assert schema["function"]["name"] in {"remember_fact", "forget_fact", "recall_fact"}
            params = schema["function"]["parameters"]
            assert params["type"] == "object"
            assert "key" in params["properties"]
            assert "key" in params["required"]

    def test_include_filter(self, tmp_path: Path):
        store = JSONMemoryStore(tmp_path / "m.json")
        tools = memory_tools(store, include=("recall_fact",))
        assert len(tools) == 1
        assert tools[0].name == "recall_fact"

    def test_include_unknown_name_raises(self, tmp_path: Path):
        store = JSONMemoryStore(tmp_path / "m.json")
        with pytest.raises(ValueError):
            memory_tools(store, include=("bogus_tool",))

    def test_two_stores_stay_isolated(self, tmp_path: Path):
        a = JSONMemoryStore(tmp_path / "a.json")
        b = JSONMemoryStore(tmp_path / "b.json")
        rem_a, _, _ = memory_tools(a)
        rem_b, _, _ = memory_tools(b)
        rem_a.call({"key": "k", "value": "from-a"})
        rem_b.call({"key": "k", "value": "from-b"})
        assert a.get_fact("k") == "from-a"
        assert b.get_fact("k") == "from-b"

    def test_scoped_remember_and_forget(self, tmp_path: Path):
        store = JSONMemoryStore(tmp_path / "m.json")
        remember, forget, _ = memory_tools(store)
        remember.call({"key": "pref", "value": "spicy", "scope": "user"})
        remember.call({"key": "pref", "value": "mild", "scope": "env:kitchen"})
        assert store.get_fact("pref", scope="user") == "spicy"
        assert store.get_fact("pref", scope="env:kitchen") == "mild"
        forget.call({"key": "pref", "scope": "user"})
        assert store.get_fact("pref", scope="user") is None
        # The kitchen-scoped fact survives — scoping is genuinely
        # honoured end-to-end.
        assert store.get_fact("pref", scope="env:kitchen") == "mild"

    def test_tools_remain_callable_after_store_reopen(self, tmp_path: Path):
        """The tool closure holds a reference to the store; reopening
        the store (fresh instance pointing at the same file) does
        NOT invalidate the closure — the old tool still writes to
        the original in-memory store. This is by design: if you want
        a different store, rebuild the tools."""
        path = tmp_path / "m.json"
        store = JSONMemoryStore(path)
        remember, _, _ = memory_tools(store)
        remember.call({"key": "k", "value": "v1"})
        store.flush()
        # Reopen under a new instance — it reads the on-disk state.
        fresh = JSONMemoryStore(path)
        assert fresh.get_fact("k") == "v1"
        # The old ``remember`` is still bound to the OLD store instance.
        remember.call({"key": "k", "value": "v2"})
        assert store.get_fact("k") == "v2"
        # The fresh store hasn't been reloaded and so still holds v1.
        assert fresh.get_fact("k") == "v1"


