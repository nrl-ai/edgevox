class TestSQLiteMemoryStore:
    def test_add_and_get_fact(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.add_fact("user.name", "Ada")
        assert s.get_fact("user.name") == "Ada"
        s.close()

    def test_overwrite_invalidates_prior_keeps_history(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.add_fact("temp", "20")
        time.sleep(0.01)
        s.add_fact("temp", "22")
        assert s.get_fact("temp") == "22"
        hist = s.fact_history("temp")
        assert len(hist) == 2
        assert hist[0].value == "20"
        assert hist[0].valid_to is not None
        assert hist[0].invalidated_at is not None
        assert hist[1].value == "22"
        assert hist[1].supersedes == hist[0].id
        s.close()

    def test_same_value_rewrite_is_noop_refresh(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.add_fact("k", "v")
        [before] = s.fact_history("k")
        old_updated = before.updated_at
        time.sleep(0.01)
        s.add_fact("k", "v")  # identical value
        hist = s.fact_history("k")
        assert len(hist) == 1
        assert hist[0].updated_at > old_updated
        s.close()

    def test_forget_fact_marks_not_active(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.add_fact("k", "v")
        assert s.forget_fact("k") is True
        assert s.get_fact("k") is None
        # History still holds it.
        assert len(s.fact_history("k")) == 1
        s.close()

    def test_forget_missing_returns_false(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        assert s.forget_fact("never") is False
        s.close()

    def test_facts_as_of_returns_historical_view(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.add_fact("mug.location", "counter")
        t_counter = time.time()
        time.sleep(0.05)
        s.add_fact("mug.location", "drawer")
        t_drawer = time.time()
        # Mid-interval: the counter fact is still believed.
        mid = (t_counter + t_drawer) / 2
        as_of_mid = s.facts_as_of(mid)
        assert len(as_of_mid) == 1
        assert as_of_mid[0].value == "counter"
        # After second write: drawer is the truth.
        as_of_now = s.facts_as_of(time.time() + 1.0)
        assert [f.value for f in as_of_now] == ["drawer"]
        s.close()

    def test_scoped_facts(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.add_fact("name", "Ada", scope="user")
        s.add_fact("name", "Kitchen", scope="env:kitchen")
        user_only = s.facts(scope="user")
        assert {f.key for f in user_only} == {"name"}
        assert user_only[0].value == "Ada"
        all_active = s.facts()
        assert len(all_active) == 2
        s.close()

    def test_preferences_upsert(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.set_preference("voice", "af_heart")
        s.set_preference("voice", "af_bella")  # upsert
        prefs = s.preferences()
        assert len(prefs) == 1
        assert prefs[0].value == "af_bella"
        s.close()

    def test_episodes_bounded_ring(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s._max_episodes = 5  # tighten for the test
        for i in range(12):
            s.add_episode("tick", {"i": i}, "ok")
        recent = s.recent_episodes(n=10)
        assert len(recent) == 5
        # Oldest retained should be i=7 (12 total - 5 kept = 7 dropped).
        payloads = [e.payload["i"] for e in recent]
        assert payloads == [7, 8, 9, 10, 11]
        s.close()

    def test_episodes_filter_by_kind(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.add_episode("grasp", {"obj": "cup"}, "ok")
        s.add_episode("navigate", {"to": "kitchen"}, "ok")
        s.add_episode("grasp", {"obj": "plate"}, "failed")
        grasps = s.recent_episodes(n=10, kind="grasp")
        assert [e.outcome for e in grasps] == ["ok", "failed"]
        s.close()

    def test_render_for_prompt_matches_shape(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.add_fact("user.name", "Ada")
        s.set_preference("lang", "en")
        s.add_episode("grasp", {"obj": "cup"}, "ok")
        rendered = s.render_for_prompt()
        assert "Known preferences" in rendered
        assert "Known facts" in rendered
        assert "Recent outcomes" in rendered
        assert "Ada" in rendered

    def test_persists_across_reopens(self, tmp_path: Path):
        db = tmp_path / "m.db"
        s1 = SQLiteMemoryStore(db)
        s1.add_fact("k", "v")
        s1.close()
        s2 = SQLiteMemoryStore(db)
        assert s2.get_fact("k") == "v"
        s2.close()

    def test_concurrent_writes_do_not_corrupt(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        errors: list[BaseException] = []

        def worker(i: int):
            try:
                for j in range(20):
                    s.add_fact(f"k{i}", f"v{i}-{j}")
            except Exception as exc:
                errors.append(exc)

        threads = [threading.Thread(target=worker, args=(i,)) for i in range(5)]
        for t in threads:
            t.start()
        for t in threads:
            t.join()
        assert errors == []
        # Each key should land on its final value.
        for i in range(5):
            assert s.get_fact(f"k{i}") == f"v{i}-19"
        s.close()

    def test_close_is_idempotent(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.close()
        s.close()  # must not raise

    def test_passes_memorystore_protocol(self, tmp_path: Path):
        """SQLiteMemoryStore is a structural fit for MemoryStore."""
        from edgevox.agents.memory import MemoryStore

        s = SQLiteMemoryStore(tmp_path / "m.db")
        assert isinstance(s, MemoryStore)
        s.close()

    def test_wal_mode_is_enabled(self, tmp_path: Path):
        """WAL mode is the main promise — concurrent readers alongside
        a writer. Verify the PRAGMA actually took effect."""
        s = SQLiteMemoryStore(tmp_path / "m.db")
        result = s._conn.execute("PRAGMA journal_mode").fetchone()
        assert result[0].lower() == "wal"
        s.close()

    def test_second_reader_sees_committed_writes(self, tmp_path: Path):
        """Two SQLiteMemoryStore instances over the same DB file should
        both see facts written by the other — the multi-process-safe
        promise in WAL mode."""
        db = tmp_path / "m.db"
        writer = SQLiteMemoryStore(db)
        writer.add_fact("k", "v1")
        reader = SQLiteMemoryStore(db)  # second connection, no cross-talk
        assert reader.get_fact("k") == "v1"
        writer.add_fact("k", "v2")
        # ``get_fact`` reads through a fresh SELECT on each call, so
        # the second reader sees the update on the next query.
        assert reader.get_fact("k") == "v2"
        writer.close()
        reader.close()

    def test_valid_from_strictly_increases_across_overwrites(self, tmp_path: Path):
        """Bi-temporal invariant: every fact's valid_from >= the prior
        one's. Tests that the time clamp in add_fact isn't trashing
        ordering on fast consecutive writes."""
        s = SQLiteMemoryStore(tmp_path / "m.db")
        for i in range(5):
            s.add_fact("k", f"v{i}")
            time.sleep(0.002)
        from itertools import pairwise

        hist = s.fact_history("k")
        assert len(hist) == 5
        for a, b in pairwise(hist):
            assert a.valid_from <= b.valid_from, "history must be chronologically ordered"
        s.close()

    def test_facts_as_of_never_returns_a_fact_before_its_valid_from(self, tmp_path: Path):
        s = SQLiteMemoryStore(tmp_path / "m.db")
        s.add_fact("k", "v")
        [only] = s.fact_history("k")
        # A timestamp strictly earlier than valid_from must yield []
        result = s.facts_as_of(only.valid_from - 1.0)
        assert result == []
        s.close()
