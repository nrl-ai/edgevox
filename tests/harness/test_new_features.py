class TestPostRequest:
    def test_reply_resolves_future(self):
        bb = Blackboard()
        fut = bb.post_request("plan.request", {"goal": "pick cup"})
        request = bb.get("plan.request")
        assert request["task"] == {"goal": "pick cup"}
        bb.reply_to(request, "mocked plan")
        assert fut.result(timeout=1.0) == "mocked plan"

    def test_volunteer_watches_request_key(self):
        """End-to-end: a volunteer watcher reacts to the post and
        sends back a reply — the canonical supervisor/worker loop."""
        bb = Blackboard()

        def volunteer(_k, _old, new):
            if isinstance(new, dict) and "task" in new:
                bb.reply_to(new, {"plan": f"executed {new['task']}"})

        bb.watch("plan.request", volunteer)
        fut = bb.post_request("plan.request", "sweep floor", timeout=1.0)
        result = fut.result(timeout=1.0)
        assert result == {"plan": "executed sweep floor"}

    def test_cancelled_future_unsubscribes_watcher(self):
        """If the caller cancels before the reply lands, no lingering
        watcher should remain — otherwise a late reply would try to
        resolve a cancelled Future and silently no-op, and the
        unbounded watcher list would leak over time."""
        bb = Blackboard()
        fut = bb.post_request("k", "t")
        reply_key = bb.get("k")["reply_key"]
        fut.cancel()
        # Wait a beat for the done-callback to run the unsubscribe.
        time.sleep(0.02)
        assert bb._watchers.get(reply_key, []) == []

    def test_late_reply_after_timeout_is_harmless(self):
        """Volunteer might finish work after the timeout expires. The
        Future is already in error state — we must not raise a second
        time or corrupt blackboard state."""
        bb = Blackboard()
        fut = bb.post_request("k", "t", timeout=0.05)
        with pytest.raises(FutureTimeout):
            fut.result(timeout=1.0)
        # A late reply after timeout is allowed (no exception from
        # the framework). It simply won't resolve the Future.
        reply_key = bb.get("k")["reply_key"]
        bb.set(reply_key, "too-late")
        # The late reply did set the blackboard key — that's ok.
        assert bb.get(reply_key) == "too-late"

    def test_custom_reply_key_is_honored(self):
        bb = Blackboard()
        fut = bb.post_request("req", "t", reply_key="my.explicit.reply")
        bb.set("my.explicit.reply", "answer")
        assert fut.result(timeout=1.0) == "answer"

    def test_reply_key_autogenerated_uniquely(self):
        bb = Blackboard()
        f1 = bb.post_request("plan", "t1")
        f2 = bb.post_request("plan", "t2")
        # Each request allocates its own reply_key, so replying to one
        # doesn't resolve the other.
        reqs = [bb.get("plan")]  # only the latest survives on the key
        assert reqs[0]["task"] == "t2"
        # Manually resolve f2's reply.
        bb.reply_to(reqs[0], "done-t2")
        assert f2.result(timeout=1.0) == "done-t2"
        assert not f1.done()
        f1.cancel()

    def test_timeout_fires_exception(self):
        bb = Blackboard()
        fut = bb.post_request("never.answered", "x", timeout=0.15)
        with pytest.raises(FutureTimeout):
            fut.result(timeout=1.0)

    def test_reply_to_rejects_non_envelope(self):
        bb = Blackboard()
        with pytest.raises(ValueError):
            bb.reply_to("not a dict", "result")
        with pytest.raises(ValueError):
            bb.reply_to({"no_reply_key": True}, "result")

    def test_watcher_unsubscribed_after_reply(self):
        bb = Blackboard()
        fut = bb.post_request("k", "t")
        assert bb.get("k")["reply_key"] in set(bb._watchers)
        bb.reply_to(bb.get("k"), "done")
        fut.result(timeout=1.0)
        # Unsub runs as a done-callback — the watcher list for that
        # reply key should be empty or gone.
        reply_key = bb.get("k")["reply_key"]
        assert bb._watchers.get(reply_key, []) == []


