class TestAsyncWatchers:
    def test_sync_blackboard_blocks_set_on_slow_watcher(self):
        bb = Blackboard()
        started = threading.Event()

        def slow(_k, _old, _new):
            started.set()
            time.sleep(0.2)

        bb.watch("x", slow)
        t0 = time.monotonic()
        bb.set("x", 1)
        elapsed = time.monotonic() - t0
        # Synchronous fan-out: ``set`` only returns after watcher finished.
        assert started.is_set()
        assert elapsed >= 0.15, f"expected sync dispatch to block ≥0.15 s, took {elapsed:.3f} s"

    def test_async_blackboard_does_not_block_set(self):
        bb = Blackboard(async_watchers=True)
        started = threading.Event()
        finished = threading.Event()

        def slow(_k, _old, _new):
            started.set()
            time.sleep(0.2)
            finished.set()

        bb.watch("x", slow)
        t0 = time.monotonic()
        bb.set("x", 1)
        set_elapsed = time.monotonic() - t0
        assert set_elapsed < 0.1, f"async ``set`` should return fast, took {set_elapsed:.3f} s"
        # The watcher still runs; wait for it.
        assert finished.wait(timeout=1.0)
        bb.close()

    def test_close_is_idempotent_on_sync_blackboard(self):
        bb = Blackboard()
        bb.close()
        bb.close()  # must not raise — pool was never created

    def test_watcher_exception_does_not_poison_pool(self):
        bb = Blackboard(async_watchers=True)
        raised = threading.Event()
        ok = threading.Event()

        def boom(_k, _old, _new):
            raised.set()
            raise RuntimeError("handler fail")

        def second(_k, _old, _new):
            ok.set()

        bb.watch("x", boom)
        bb.watch("x", second)
        bb.set("x", 1)
        assert raised.wait(timeout=1.0)
        assert ok.wait(timeout=1.0)
        bb.close()

    def test_many_concurrent_writes_land_all_watchers(self):
        """Under async mode, N simultaneous writers should each
        deliver to the watcher. No dropped events, no deadlock."""
        bb = Blackboard(async_watchers=True, max_watcher_workers=4)
        seen: list[int] = []
        lock = threading.Lock()

        def record(_k, _old, new):
            with lock:
                seen.append(new)

        bb.watch("x", record)

        def writer(n: int):
            for i in range(n):
                bb.set("x", i)

        threads = [threading.Thread(target=writer, args=(10,)) for _ in range(5)]
        for t in threads:
            t.start()
        for t in threads:
            t.join()
        # Give the executor a moment to drain its queue.
        bb.close()
        assert len(seen) == 50

    def test_delete_triggers_watchers_too(self):
        """delete() should fan out just like set() — otherwise watchers
        can't observe cleanup events."""
        bb = Blackboard()
        observed: list[tuple] = []
        bb.set("k", 1)
        bb.watch("k", lambda k, old, new: observed.append((k, old, new)))
        deleted = bb.delete("k")
        assert deleted is True
        assert observed == [("k", 1, None)]


